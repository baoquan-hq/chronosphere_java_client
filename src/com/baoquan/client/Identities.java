package com.baoquan.client;

public class Identities {

	private String ID;

	private String MO;

	public Identities() {
	}

	public Identities(String ID, String MO) {
		this.ID = ID;
		this.MO = MO;
	}

	public String getID() {
		return ID;
	}

	public void setID(String ID) {
		this.ID = ID;
	}

	public String getMO() {
		return MO;
	}

	public void setMO(String mO) {
		MO = mO;
	}

}
